function getInfo(postalCode: string) {
  const zone = ZONE_CPS[postalCode];
  if (zone !== undefined) {
    const price = ZONE_PRICES[zone];
    return {
      valid: true,
      zone,
      price,
      hasPickup: zone !== 0,
      message: zone === 0
        ? "Tu CP requiere cotización especial. Contáctanos al 55 8237 5469."
        : (price !== null && price >= 690)
          ? `Tu zona tiene un costo de envío de $${price}. Puedes ahorrar recogiendo en Lago Onega 265, Modelo Pensil, CDMX.`
          : null,
    };
  }
  // Default: zone 3
  return { valid: true, zone: 3, price: 360, hasPickup: true, message: "Envío estándar para tu zona." };
}

Deno.serve(async (req: Request) => {
  if (req.method === "OPTIONS") {
    return new Response("ok", { headers: corsHeaders });
  }

  try {
    const { postalCode } = await req.json();
    if (!postalCode || typeof postalCode !== "string" || postalCode.length !== 5) {
      return new Response(JSON.stringify({ valid: false, error: "CP inválido" }), {
        headers: { ...corsHeaders, "Content-Type": "application/json" },
        status: 400,
      });
    }

    const info = getInfo(postalCode);
    return new Response(JSON.stringify(info), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  } catch {
    return new Response(JSON.stringify({ valid: false, error: "Error al validar" }), {
      headers: { ...corsHeaders, "Content-Type": "application/json" },
      status: 500,
    });
  }
});
